#include <stdlib.h>
/**
 * str_concat - concatenates two strings
 * @s1: string 1
 * @s2: string 2
 */
char *str_concat(char *s1, char s2)
{
	int s1len;
	int s2len;

	if (s1 == NULL)
	{
		s1 = "";
	}
	if (s2 == NULL)
	{
		s2 = "";
	}
	for (s1len = 0; s1[s1len] != '\0'; s1len++)
	{
		continue;
	}
	for (s2len = 0; s2[s2len] != '\0'; s2len++)
	{
		continue;
	}
	
	return (NULL);
}
